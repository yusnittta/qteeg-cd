from enum import Enum    #Mengimpor kelas Enum dari modul enum untuk membuat enumerasi
                                                          
class Frequency(Enum):   #Mendefinisikan kelas Frequency yang merupakan enumerasi untuk berbagai jenis frekuensi
    GAMMA = 1            #Menetapkan nilai 1 untuk konstanta enumerasi GAMMA
    BETA = 2             #Menetapkan nilai 2 untuk konstanta enumerasi BETA
    ALPHA = 3            #Menetapkan nilai 3 untuk konstanta enumerasi ALPHA
    THETA = 4            #Menetapkan nilai 4 untuk konstanta enumerasi THETA
    DELTA = 5            #Menetapkan nilai 5 untuk konstanta enumerasi DELTA